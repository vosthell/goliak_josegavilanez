    public void cargarDataCliente()
    {
        ArrayList<clsCliente> dataCliente = objCliente.consultarDataCliente(txtCedulaDestinatario.getText().toString());
        if(dataCliente.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Cédula no existe!!!", "Atención!", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            vaciarDatos();
            txtCedulaDestinatario.setText(dataCliente.get(0).getCedula());
            codigoDestinatario = dataCliente.get(0).getCodigo();            
            txtDestinatario.setText(dataCliente.get(0).getNameCompleto());
            txtCodigoProducto.requestFocus();
        }
    }
     
