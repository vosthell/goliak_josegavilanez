private void txtPassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPassKeyPressed
    String pass = "";
    if(evt.getKeyChar() == KeyEvent.VK_ENTER){ 
        char passArray[] = this.txtPass.getPassword();
        pass = new String(passArray);
        //JOptionPane.showMessageDialog(this, "Texto:"+pass, "Atenci贸n!", JOptionPane.ERROR_MESSAGE);
        //COMPROBAR QUE ES USUARIO DE TARJETA
        boolean exito = objUsuario.consultarExistenciaCardMaster(pass);
        if(exito)
        {
            ArrayList<clases.clsUsuario> dataUsuario = objUsuario.consultaDataUsuarioCard(pass);
            frmFacturar.idUserCard = dataUsuario.get(0).getIdUsuario();
            frmFacturar.txtDescuento.setEditable(true);
            frmFacturar.lblDescuento.setText("DESCTO AUT. POR: " + dataUsuario.get(0).getNombre());
            frmFacturar.txtDescuento.setText("");
            frmFacturar.txtDescuento.requestFocus();
            dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "USUARIO INCORRECTO", "Atenci贸n!", JOptionPane.ERROR_MESSAGE);
        }
    }
    /*if ((evt.getKeyCode() == KeyEvent.VK_V) && ((evt.getModifiers() & KeyEvent.CTRL_MASK) != 0)) {
        JOptionPane.showMessageDialog(this, "TECLAS DESHABILITADAS", "Atenci贸n!", JOptionPane.ERROR_MESSAGE);
        txtPass.setText("");
    }*/
    
    if ((evt.getModifiers() & KeyEvent.CTRL_MASK) != 0) {
        JOptionPane.showMessageDialog(this, "TECLA DESHABILITADA", "Atenci贸n!", JOptionPane.ERROR_MESSAGE);
        txtPass.setText("");
    }
}//GEN-LAST:event_txtPassKeyPressed

