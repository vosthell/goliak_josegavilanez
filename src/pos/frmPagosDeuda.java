    public frmPagosDeuda(String va1, String va2, String valor, String saldo, String fecha_compra) {
        initComponents();
        
        Double totalDeuda = 0.00;
        
        dtmData.addColumn("NÂ°");/*.setPreferredWidth(500)*/
        dtmData.addColumn("Fecha");
        dtmData.addColumn("Valor Pago");
        //1:3meses, 2:6 meses, 3: 9 meses, 4 :12 meses
        int plazo = Integer.parseInt(va1);
         //1:diario, 2:semanal, 3: quincenal, 4 :mensual
        int cuotas = Integer.parseInt(va2);
        int meses =0;
        if(plazo==1)
        {   
            meses = 3;
            txtPlazo.setText("3 MESES");
        }
        if(plazo==2)
        {
            meses = 6;
            txtPlazo.setText("6 MESES");
        }            
        if(plazo==3)
        {    
            meses = 9;
            txtPlazo.setText("9 MESES");
        }
        if(plazo==4)
        {    
            meses = 12;
            txtPlazo.setText("12 MESES");
        }
        int dias=0;
        if(cuotas==1)
        {   
            dias = 30;
            txtCuota.setText("DIARIO");
        }
        if(cuotas==2)
        {
            dias = 4;
            txtCuota.setText("SEMANAL");
        }
        if(cuotas==3)
        {
            dias =2;
            txtCuota.setText("QUINCENAL");
        }             
        if(cuotas==4)
        {
            dias = 1;
            txtCuota.setText("MENSUAL");
        }                
                    
        //int total = meses * dias;
        Double numeroPagos = Double.parseDouble(saldo) / Double.parseDouble(valor);
        DecimalFormat formateador = new DecimalFormat("####");
        Integer numeroPagosRedondeado = Integer.parseInt(formateador.format(numeroPagos)); 
        //establecer la fecha de hoy
        Date date1= new Date();
        Calendar fecha = Calendar.getInstance();
        fecha.setTime(date1); 
        
        
        //for(int i=0; i<total; i++)
        for(int i=0; i<numeroPagosRedondeado; i++)
        {           
            if(va2.equals("1"))       
            {
                //DIARIO
                fecha.add(Calendar.DAY_OF_MONTH, 1); 
            }
            else if(va2.equals("2"))  
            {
                //SEMANAL
                fecha.add(Calendar.DAY_OF_MONTH, 7);
            }
            else if(va2.equals("3"))  
            {
                //QUINCENAL
                fecha.add(Calendar.DAY_OF_MONTH, 15); 
            }
            else if(va2.equals("4"))  
            {
                //MENSUAL
                fecha.add(Calendar.MONTH, 1);
            }
            
            Date fecSegCarta = fecha.getTime();
            date1 = fecSegCarta;
            DateFormat df2 = new SimpleDateFormat("yyyy-MM-dd");
            String fecha2 = df2.format(fecSegCarta);
            
            Object[] nuevaFila = {i, fecha2, valor};
            dtmData.addRow(nuevaFila);
            totalDeuda = totalDeuda + Double.parseDouble(valor);
        }
        
        txtTotal.setText("" + totalDeuda);
        
        
        
    }
    
