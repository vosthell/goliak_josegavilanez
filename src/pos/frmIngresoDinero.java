private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    if(txtConcepto.getText().equals("")||txtValor.getText().equals(""))
    {
        JOptionPane.showMessageDialog(this, "Ingrese correctamente la información", "Atención!", JOptionPane.ERROR_MESSAGE);           
    }
    else
    {
        boolean exito = objEgreso.insertarValorCuota(idCajaAbierta, txtConcepto.getText(), 
                                                txtValor.getText(), "I");    
        if(exito)
        {
             JOptionPane.showMessageDialog(this, "Ingresado con éxito", "Atención!", JOptionPane.INFORMATION_MESSAGE); 
             abrirCaja();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error al ingresar", "Atención!", JOptionPane.ERROR_MESSAGE);    
        }
        dispose();
    }    
}//GEN-LAST:event_btnGuardarActionPerformed

    public void abrirCaja()
    {
        /*ABRIR LA CAJA, MANDO A IMPRIMIR CODIGO ESPECIAL A LA IMPRESORA TMU*/
        FileWriter fichero2 = null;
        PrintWriter pw2 = null;
        try
        {
            
            fichero2 = new FileWriter(objUtils.HostSystem + objUtils.archivoPrint2);
            pw2 = new PrintWriter(fichero2);
            byte[] bit = new byte[1];
            bit[0] = (byte)27;
            String a = new String(bit);
            //byte[] bit = new byte[1];
            bit[0] = (byte)112;
            String b = new String(bit);            
            pw2.println(a + b + 0);
            /*******ABRIR CAJON****/
            Runtime aplicacion2 = Runtime.getRuntime();             
            aplicacion2.exec("cmd.exe /K "+ objUtils.HostSystem + objUtils.abrirCaja); 
        } 
        catch (Exception e) 
        {
            System.out.println(e.toString());
            e.printStackTrace();
        } 
        finally 
        {
           try {
           // Nuevamente aprovechamos el finally para 
           // asegurarnos que se cierra el fichero.
           if (null != fichero2)
              fichero2.close();
           } catch (Exception e2) {
              System.out.println(e2.toString());
              //e2.printStackTrace();
           }
        }
        /*FIN - ABRIR LA CAJA, MANDO A IMPRIMIR CODIGO ESPECIAL A LA IMPRESORA TMU*/
    }

