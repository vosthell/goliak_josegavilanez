    main.ipSeleccionada = objEmpresa.consultaIPEmpresaSeleccionada(objEmpresaSelect.getCodigo());
    
    System.out.println(main.ipSeleccionada);
    
    char passArray[] = txtClave.getPassword();
    String pass = new String(passArray);
    //boolean exito = objUsuario.consultarExistencia(pass.toUpperCase());
    boolean exito = objUsuario.consultarExistencia(pass);
    System.out.println("A: " + exito);
    if(exito)
    {
        //ArrayList<pos.clases.clsUsuario> dataUsuario = objUsuario.consultaDataUsuario(pass.toUpperCase());
        ArrayList<clases.clsUsuario> dataUsuario = objUsuario.consultaDataUsuario(pass);
        System.out.println("c:" + dataUsuario.size());
        if(dataUsuario.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No tiene asignados permisos", "Atención!", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {    
            main.idUser = dataUsuario.get(0).getIdUsuario() + "";
            main.nameUser = dataUsuario.get(0).getNombre();  
            /* frmMenu ventana = new frmMenu();
            ventana.setLocationRelativeTo(null);
            ventana.setVisible(true);*/
            stinventario.frmPrincipal ventana = new stinventario.frmPrincipal();
            ventana.setLocationRelativeTo(null);
            ventana.setExtendedState(JFrame.MAXIMIZED_BOTH);
            ventana.setVisible(true);
            dispose();     
        }
    }
    else
    {
        lblMensaje.setText("Contraseña incorrecta");
    }    
