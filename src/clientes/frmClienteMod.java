    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ArrayList<clsCliente> dataCliente = objCliente.consultaDataCliente(txtCedula.getText().toString()); 
        if(dataCliente.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No existe Cliente", "Atenci√≥n!", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            codigoCliente = dataCliente.get(0).getCodigo();    
            txtID.setText("" + codigoCliente);
            txtNombreCompleto.setText(dataCliente.get(0).getNameCompleto());
            txtNombre1.setText(dataCliente.get(0).getNombre1());
            txtNombre2.setText(dataCliente.get(0).getNombre2());
            txtApellido1.setText(dataCliente.get(0).getApellido1());
            txtApellido2.setText(dataCliente.get(0).getApellido2());
            txtConvencional.setText(dataCliente.get(0).getTlfConvencional());
            txtCelular.setText(dataCliente.get(0).getTlfCelular());
            txtDireccion.setText(dataCliente.get(0).getDireccion());
            //txtCiudad.setText(dataCliente.get(0).getCiudad());
            txtCredito.setText(dataCliente.get(0).getCreditoMax());
            control = "0"; 
            //fecha de nacimiento
           /* SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yyyy-MM-dd");
            String strFecha = dataCliente.get(0).getFechaNacimiento();
            Date fecha = null;
            try {

                fecha = formatoDelTexto.parse(strFecha);

            } catch (Exception ex) {
                ex.printStackTrace();
            }
            txtFechaNacimiento.setDate(fecha); */           
            
            //CARGAR PROVINCIAS
            cmbProvincia.removeAllItems();
            ArrayList<clsComboBox> dataProvincia = objProvincia.consultarProvincias();        
            for(int i=0;i<dataProvincia.size();i=i+1)
            {
                clsComboBox oItem = new clsComboBox(dataProvincia.get(i).getCodigo(), dataProvincia.get(i).getDescripcion());
                cmbProvincia.addItem(oItem);
                if(Integer.parseInt(dataProvincia.get(i).getCodigo())==(dataCliente.get(0).getProvincia()))
                {    
                    cmbProvincia.setSelectedItem(oItem);
                }    
            }

            //CARGAR CIUDAD
            cmbCiudad.removeAllItems();
            clsComboBox objProvinciaSelect = (clsComboBox)cmbProvincia.getSelectedItem();
            ArrayList<clsComboBox> dataCiudad = objCiudad.consultarCiudad(objProvinciaSelect.getCodigo());        
            for(int i=0;i<dataCiudad.size();i=i+1)
            {
                clsComboBox oItem = new clsComboBox(dataCiudad.get(i).getCodigo(), dataCiudad.get(i).getDescripcion());
                cmbCiudad.addItem(oItem);
                if(Integer.parseInt(dataCiudad.get(i).getCodigo())==(dataCliente.get(0).getIdCiudad()))
                {    
                    cmbCiudad.setSelectedItem(oItem);
                }    
            }
            control = "1";       
            //CARGAR TERMINOS
            cmbTermino.removeAllItems();
            ArrayList<clsComboBox> dataTerminos = objTermino.consultarTerminos();        
            for(int i=0;i<dataTerminos.size();i=i+1)
            {
                clsComboBox oItem = new clsComboBox(dataTerminos.get(i).getCodigo(), dataTerminos.get(i).getDescripcion());
                cmbTermino.addItem(oItem);  

                if(Integer.parseInt(dataTerminos.get(i).getCodigo())==(dataCliente.get(0).getIdTermino()))
                {    
                    cmbTermino.setSelectedItem(oItem);
                } 
            }

             //CARGAR RECINTOS
            cmbRecinto.removeAllItems();
            ArrayList<clsComboBox> dataRecintos = objRecinto.consultarRecintos();        
            for(int i=0;i<dataRecintos.size();i=i+1)
            {
                clsComboBox oItem = new clsComboBox(dataRecintos.get(i).getCodigo(), dataRecintos.get(i).getDescripcion());
                cmbRecinto.addItem(oItem);  

                if(dataRecintos.get(i).getCodigo().equals(dataCliente.get(0).getIdRecinto()))
                {    
                    cmbRecinto.setSelectedItem(oItem);
                } 
            }
        }        
    }//GEN-LAST:event_btnBuscarActionPerformed

