     public void llenarData(ArrayList<clsCabecera> dataCabecera)
    {
        Double total = 0.00;
        Double totalEfectivo = 0.00;
        Double totalSaldo = 0.00;
        
        DecimalFormat df1 = new DecimalFormat("##0.00"); 
        int maxData = dataCabecera.size();
        String fecha_realizada = "";
        
        for(int i=0; i<maxData; i++)
        {
            lblVendedor.setText(dataCabecera.get(i).getNombreVendedor());
            fecha_realizada = dataCabecera.get(i).getFecha().substring(1, 16);                     
            Object[] nuevaFila = {  i+1,                                     
                                    dataCabecera.get(i).getIdCabeceraMovi(),
                                    dataCabecera.get(i).getNameCompleto(),
                                    fecha_realizada,
                                    dataCabecera.get(i).getNombreVendedor(),
                                    df1.format(dataCabecera.get(i).getTotal()),                                    
                                    df1.format(dataCabecera.get(i).getEfectivo()),    
                                    df1.format(dataCabecera.get(i).getSaldo()), 
                                    dataCabecera.get(i).getFactReferencia()                                    
             };       
             total = total + dataCabecera.get(i).getTotal();
             totalEfectivo = totalEfectivo + dataCabecera.get(i).getEfectivo();
             totalSaldo = totalSaldo + dataCabecera.get(i).getSaldo();
             dtmData.addRow(nuevaFila); 
        } 
        txtEfectivo.setText(""+objUtils.redondear(totalEfectivo));
        txtCredito.setText(""+objUtils.redondear(totalSaldo));
        txtTotal.setText("" + (objUtils.redondear(totalEfectivo + totalSaldo)));
        //tblData.setDefaultRenderer (Object.class, new colorFilaTable());
    }
     
    public void buscar_informacion(String idVendedor, String fecha, String fechaInicio, String fechaFin)
    {
        objUtils.vaciarTabla(dtmData);
        txtEfectivo.setText("");
        
         String soloCredito = "N";

        if(fecha.equals("SI"))
        {
            ArrayList<clsCabecera> dataCompras = objCabecera.consultaDataVentasRangoNE(idVendedor, fechaInicio, fechaFin, soloCredito); 
            llenarData(dataCompras);
        }       
        else
        {
            ArrayList<clsCabecera> dataCompras = objCabecera.consultaDataVentasNEVende(idVendedor, soloCredito); 
            llenarData(dataCompras);    
        }
    }        
